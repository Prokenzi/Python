broj_1 = float(input("Unesite vrednost prvog broja: "))
broj_2 = float(input("Unesite vrednost drugog broja: "))

# print(type(broj_1))
# print(type(broj_2))

zbir = broj_1 + broj_2
razlika = broj_1 - broj_2
mnozenje = broj_1 * broj_2
deljenje = broj_1 / broj_2

print("Zbir unesenih brojeva:" + str(zbir))
print("Razlika unesenih brojeva:" + str(razlika))
print("Mnozenje unesenih brojeva: " + str(mnozenje))
print("Kolicnik unesenih brojeva: " + str(deljenje))